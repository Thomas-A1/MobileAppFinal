      child: Column(
        children: [
          Image(
            width: screenWidth * 0.8,
            height: screenHeight * 0.6,
            image:
                // AssetImage(image),
                AssetImage(image),
          ),
          // title
          Text(
            title,
            style: Theme.of(context).textTheme.headlineMedium?.copyWith(
